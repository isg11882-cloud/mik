/**
 * 재회컨설팅 AI — 위시아 수석 컨설턴트 '재이'
 * 지식베이스(이론 + 성공 패턴)가 완전 통합된 고품질 시스템 프롬프트
 */

export interface UserContext {
  breakupType: 'A' | 'B' | 'C' | 'D' | null
  daysSinceBreakup: number
  currentPhase: 1 | 2 | 3
  userName?: string
  gender?: 'male' | 'female'
  partnerGender?: 'male' | 'female'
  situation?: string
  /** 사용자가 직접 적은 자유 메모 (profiles.situation_memo) — Phase 2-A */
  situationMemo?: string
  coachingStyle?: 'healing' | 'analytical' | 'action'
}

/**
 * daysSinceBreakup 기준으로 PHASE 를 재계산.
 * 진단 시점의 phase 가 아니라 "지금 시점" 기준이라 매 채팅 요청마다 자동 갱신됨.
 */
export function phaseFromDays(days: number): 1 | 2 | 3 {
  if (days < 30) return 1
  if (days < 90) return 2
  return 3
}

